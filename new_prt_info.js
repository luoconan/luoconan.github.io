function getForDAR() {
    let res = {};
    Object.keys(prtObjInfo).forEach(prtName => {
        res[prtName.toUpperCase()] = prtObjInfo[prtName].id;
    })
    return res;
}

function getPrtsArr() {
    let prts = [];
    Object.keys(prtObjInfo).forEach(prtInfo => {
        let temp = {};
        temp.name = prtInfo;
        Object.keys(prtObjInfo[prtInfo]).forEach(info => {
            temp[info] = prtObjInfo[prtInfo][info];
        })

        prts.push(temp);
    })
    return prts;
}
